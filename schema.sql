 -- STATEMENT OF ACCOUNT TABLE
 
 
 CREATE TABLE STATEMENT_OF_ACCOUNT (
    SOA_ID INT PRIMARY KEY,
    EMP_ID INT,
    CUSTOMER_ID INT,
    SOA_PERIOD VARCHAR(20),
    SOA_DATE DATE,
    SOA_BALANCE DECIMAL(10, 2),
    SOA_DETAILS VARCHAR(255),
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE (EMP_ID), 
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER (CUSTOMER_ID)
);
 
--
SELECT * FROM STATEMENT_OF_ACCOUNT

 -- SUBSCRIPTION TABLE 
 
 CREATE TABLE SUBSCRIPTION (
    SUBSCRIPTION_ID INT PRIMARY KEY,
    SERVICE_ID INT, -- (PRODUCT_ID) wrong attribute on ERD, replaced with SERVICE_ID
    CUSTOMER_ID INT,
    SUBSCRIPTION_STATUS VARCHAR(10),
    SUBSCRIPTION_START_DATE DATE,
    SUBSCRIPTION_END_DATE DATE,
    FOREIGN KEY (SERVICE_ID) REFERENCES SERVICE (SERVICE_ID), -- (PRODUCT ID) replaced with SERVICE_ID
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER (CUSTOMER_ID)
);

