 -- CUSTOMER TABLE

CREATE TABLE CUSTOMER (
    CUSTOMER_ID VARCHAR(20) PRIMARY KEY,
    CUSTOMER_STATUS VARCHAR(10),
    CUSTOMER_CONTACT INT CHECK (CUSTOMER_CONTACT > 0),
    CUSTOMER_EMAIL VARCHAR(100),
    CUSTOMER_ADDRESS VARCHAR(200)
);

--
SELECT * FROM CUSTOMER

 -- STATEMENT OF ACCOUNT TABLE
 
 
 CREATE TABLE STATEMENT_OF_ACCOUNT (
    SOA_ID VARCHAR(20) PRIMARY KEY,
    EMP_ID VARCHAR(20),
    CUSTOMER_ID VARCHAR(20),
    SOA_PERIOD VARCHAR(20),
    SOA_DATE DATE,
    SOA_BALANCE DECIMAL(10, 2),
    SOA_DETAILS VARCHAR(255),
    FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE (EMP_ID),
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER (CUSTOMER_ID)
 
--
SELECT * FROM STATEMENT_OF_ACCOUNT

 -- SUBSCRIPTION TABLE 
 
CREATE TABLE SUBSCRIPTION (
    SUBSCRIPTION_ID VARCHAR(20) PRIMARY KEY,
    SERVICE_ID VARCHAR(20),
    CUSTOMER_ID VARCHAR(20),
    SUBSCRIPTION_STATUS VARCHAR(10),
    SUBSCRIPTION_START_DATE DATE, 
    SUBSCRIPTION_END_DATE DATE,
    FOREIGN KEY (SERVICE_ID) REFERENCES SERVICE (SERVICE_ID),
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER (CUSTOMER_ID)
);

--
SELECT * FROM SUBSCRIPTION

 -- SERVICE TABLE 
 
CREATE TABLE SERVICE (
    SERVICE_ID VARCHAR(20) PRIMARY KEY,
    SERVICE_NAME VARCHAR(100),
    SERVICE_TYPE VARCHAR(50),
    SERVICE_DETAILS VARCHAR(255),
    SERVICE_PRICE DECIMAL(10, 2),
    SERVICE_EQUIPMENT VARCHAR(100)
);
 
